package com.test.bo;

public class CargoConfirmacion {
	private String fecha;
	private String importe;
	private String concepto;
	private String reconoce;
	private String idMultiple;
	private String importeCorrecto;
	private String importeDevolucion;
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	public String getImporte() {
		return importe;
	}
	public void setImporte(String importe) {
		this.importe = importe;
	}
	public String getConcepto() {
		return concepto;
	}
	public void setConcepto(String concepto) {
		this.concepto = concepto;
	}
	public String getReconoce() {
		return reconoce;
	}
	public void setReconoce(String reconoce) {
		this.reconoce = reconoce;
	}
	public String getIdMultiple() {
		return idMultiple;
	}
	public void setIdMultiple(String idMultiple) {
		this.idMultiple = idMultiple;
	}
	public String getImporteCorrecto() {
		return importeCorrecto;
	}
	public void setImporteCorrecto(String importeCorrecto) {
		this.importeCorrecto = importeCorrecto;
	}
	public String getImporteDevolucion() {
		return importeDevolucion;
	}
	public void setImporteDevolucion(String importeDevolucion) {
		this.importeDevolucion = importeDevolucion;
	}
	

}
